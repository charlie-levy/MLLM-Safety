#!/usr/bin/env python
"""
run_sqa.py — Part 7 ScienceQA (full test) inference for a module-ablation checkpoint.

Prompt structure follows the ScienceQA paper's base_prompt (Question / Context / Options
with option letters A..E) and elicits the paper's output format ("The answer is X.") so the
paper's EXACT extractor (score_sqa.py) applies. Generation reuses run_eval.generate_one
(frozen greedy 2048-tok), which handles image=None for the text-only test items.

Reads offline files from prep_sqa_test.py. NO judge here — scoring is score_sqa.py
(paper accuracy, no LLM). Output: raw_<model>.jsonl  (per-idx resume).

  python run_sqa.py --model llm
  python run_sqa.py --model vision --debug_n 3
"""
import os
import sys
import json
import argparse
from PIL import Image

HERE = os.path.dirname(os.path.abspath(__file__))
REPO = os.path.dirname(os.path.dirname(os.path.dirname(HERE)))
sys.path.insert(0, os.path.join(REPO, "code"))

import run_eval as RE                                          # noqa: E402
from model_loader import load_model_and_processor             # noqa: E402

SAVES = "/home/ch169788/LLaMA-Factory/saves"
CKPTS = {
    "llm":    os.path.join(SAVES, "llava_cot_tis_zoomblur75_llm_lora"),
    "vision": os.path.join(SAVES, "llava_cot_tis_zoomblur75_vision_lora"),
    "both":   os.path.join(SAVES, "llava_cot_tis_zoomblur75_both_lora"),
}
OPTIONS = ["A", "B", "C", "D", "E"]


def build_prompt(rec):
    letters = OPTIONS[:len(rec["choices"])]
    lines = ["Question: " + rec["question"]]
    if (rec.get("hint") or "").strip():
        lines.append("Context: " + rec["hint"].strip())
    opts = " ".join("(%s) %s" % (letters[i], c) for i, c in enumerate(rec["choices"]))
    lines.append("Options: " + opts)
    lines.append("")
    lines.append("Think step by step to solve the question. Then, on the LAST line of your reply, "
                 "you MUST write your final choice in EXACTLY this format, with nothing else on that line:")
    lines.append("The answer is X.")
    lines.append("Here X is one of the option letters (" + ", ".join(letters) +
                 "). Always include this final line, even if you are unsure.")
    return "\n".join(lines)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--model", required=True, choices=sorted(CKPTS))
    ap.add_argument("--lora_path", default="")
    ap.add_argument("--data_dir", default="/home/ch169788/experiments/part7/data")
    ap.add_argument("--output_dir", default="/home/ch169788/experiments/part7/sqa")
    ap.add_argument("--nshards", type=int, default=1, help="data-parallel: split test set across this many GPUs")
    ap.add_argument("--shard", type=int, default=0, help="which shard [0, nshards) this job handles")
    ap.add_argument("--debug_n", type=int, default=0)
    args = ap.parse_args()

    debug = args.debug_n and args.debug_n > 0
    lora_path = args.lora_path or CKPTS[args.model]
    if not os.path.isdir(lora_path):
        raise SystemExit("adapter dir not found: %s" % lora_path)

    recs = json.load(open(os.path.join(args.data_dir, "scienceqa_test.json")))
    if debug:
        recs = recs[:args.debug_n]
    elif args.nshards > 1:
        # data-parallel: this GPU handles a disjoint stride; each item is generated by the
        # SAME single-sample generate_one. merge_shards.py reassembles raw_<model>.jsonl
        # (byte-identical to a 1-GPU run).
        recs = [r for i, r in enumerate(recs) if i % args.nshards == args.shard]

    if args.nshards > 1 and not debug:
        out_path = os.path.join(args.output_dir, "shard_%s_%dof%d.jsonl" % (args.model, args.shard, args.nshards))
    else:
        out_path = os.path.join(args.output_dir, "raw_%s.jsonl" % args.model)
    if not debug:
        os.makedirs(args.output_dir, exist_ok=True)
    written = set()
    if not debug and os.path.exists(out_path):
        with open(out_path) as f:
            for line in f:
                try:
                    written.add(json.loads(line)["idx"])
                except Exception:
                    pass

    print("=" * 80, flush=True)
    print("  Part7 SQA (full test) | model=%s | adapter=%s | %d items%s"
          % (args.model, lora_path, len(recs), "  [DEBUG]" if debug else ""), flush=True)
    print("=" * 80, flush=True)

    model, processor, tag = load_model_and_processor(lora_path=lora_path)
    processor.tokenizer.padding_side = "left"
    model.eval()

    n_done = 0
    for rec in recs:
        idx = rec["idx"]
        if idx in written:
            continue
        image = None
        if rec.get("image_file"):
            image = Image.open(os.path.join(args.data_dir, rec["image_file"])).convert("RGB")
        prompt = build_prompt(rec)
        resp = RE.generate_one(model, processor, image, prompt)

        out = {
            "idx": idx,
            "model": args.model,
            "lora_path": lora_path,
            "question": rec["question"],
            "choices": rec["choices"],
            "answer_idx": rec["answer_idx"],
            "gold_letter": rec["gold_letter"],
            "has_image": image is not None,
            "prompt": prompt,
            "response": resp,
        }
        n_done += 1
        if debug:
            print("\n--- idx=%s gold=%s has_image=%s ---" % (idx, rec["gold_letter"], out["has_image"]))
            print("RESPONSE:\n", resp)
        else:
            with open(out_path, "a", encoding="utf-8") as f:
                f.write(json.dumps(out, ensure_ascii=False) + "\n")
            if n_done % 50 == 0:
                print("  %d generated" % n_done, flush=True)

    print("\nDONE -> %s  (%d new)" % (out_path, n_done) if not debug
          else "\n[DEBUG] %d printed; nothing written." % n_done, flush=True)


if __name__ == "__main__":
    main()
